    String nm,pn,ml,pwd;
    boolean validation()
    {   nm=txtname1.getText();
        pn=txtphn1.getText();
        ml=txtmail1.getText();
        pwd=txtpaswd.getText();
        
         if(nm.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter Full name");
            return false;
        }
         if(pn.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter phone number");
            return false;
        }
          if(ml.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter valid E-mail Id");
            return false;
        }
           if(pwd.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please enter 8 digit password");
            return false;
        }
        return true;
    }
