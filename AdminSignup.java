    void password()
    {   
        pwd=txtpaswd.getText();
        if(pwd.length()<7)
        {
            lblpwd.setText("*Please enter 8 digit password");
        }
        else
        {
            lblpwd.setText("");
        }
    }
    void contact()
    {
        pn=txtphn1.getText();
        if(pn.length()==9)
        {
            lblpn.setText("");
        }
        else
        {
            lblpn.setText("*Please enter 10 digit phone number");
        }
    }
