    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            cons();
            validation();
            String name=txtname1.getText();
            String phn=txtphn1.getText();
            String mail=txtmail1.getText();
            String pswd=txtpaswd.getText();
            String tlp=txtmail5.getText();
            String padd=txtmail3.getText();
            String perad=txtmail4.getText();
            String gndr=txtmail7.getText();
            String dob=txtmail2.getText();
            String msts=txtmail6.getText();
            PreparedStatement ps = con.prepareStatement("insert into admin_details(name,email,dob,phone,telephone,permanent_address,present_address,marital_status,gender,password) values(?,?,?,?,?,?,?,?,?,?)");

           
            ps.setString(1,  name);
            ps.setString(2, mail);
            ps.setString(3, dob);
            ps.setString(4, phn);
            ps.setString(5, tlp);
            ps.setString(6, perad);
            ps.setString(7, padd);
            ps.setString(8, msts);
            ps.setString(9, gndr);
            ps.setString(10, pswd);
            

            ps.executeUpdate();
            
            JOptionPane.showMessageDialog(rootPane, "Entry Added Sucessfully");
            con.close();
            AdminDashboard a=new AdminDashboard();
            dispose();
            a.show();
        }

        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

